
//  code conventions and the naming rules for variables

// meaningful variable names
let firstName = 'bob';
let emailAddress = 'bob@gmail.com';

// Camel casing
let zipCode = 10;
let ZipCode = 20;
console.log(zipCode + ZipCode);


/* We use const whenever we want to express our intent to 
the javascript compiler that we do not intend for that variable 
to ever change its value. */
const j = 5;

// the variable itself does not have a datatype
let x;
console.lot(x); //---> undefined
console.log(typeof x); //---> undefined

// A data type is really just the kind of data that we want to store






